package OOPS;

public class Greetings {
    //No param
    void m1() {
        System.out.println("Hello");
    }

    //no param but returns value
    int m2() {
        return 55;
    }

    //Params and Returns Value
    String m3(String java) {
        return java;
    }

    //    Params but no return
    void m4(char ch) {
        System.out.println(ch);
    }
}